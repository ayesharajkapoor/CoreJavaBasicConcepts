package LoopingStatement;

public class LoopingExample {

	public static void main(String[] args) {
		/*
		 * //for loop - Intialization:Conditon;Increment/Decrement for(int i=0;i<25;i++)
		 * { System.out.println("I can learn Program!"); } }
		 */

		/*
		 * // While Loop int i = 0; while (i < 25) {
		 * System.out.println("I can learn Program!"); i++; }
		 */

		
		// do while loop
		int i=0;
		do {
			System.out.println("I can learn Program!"); 
			i++;
		}
		while(i<25);
	}
}
